# Python
from datetime import date
from typing import Optional

# Pydantic
from pydantic import Field

#Models
from models.UserBase import UserBase

class User(UserBase):
    first_name: str = Field(
        ...,
        min_length=1,
        max_length=50
    )
    last_name: str = Field(
        ...,
        min_length=1,
        max_length=50
    )
    birth_date: Optional[date] = Field(
        default=None
    )